#include "main.h"

/**
 * print_alphabet - function to print alphabet in lower case
 * Return: 0 (Success)
 */

void print_alphabet(void);
{
        char i;

        for(i = 'a' ; i <= 'z' ; 1++);
                _putchar(i);
        _putchar('\n');
}
